// ============================================================================
// SCHOLARLY PLATFORM — Sprint 20, Deliverable S20-001
// S3 + CloudFront + Secrets Management
// ============================================================================
//
// Sprint 19 provisioned the database and cache — the warehouse where data
// lives. This sprint provisions the content delivery layer — the fleet of
// trucks that carries storybook illustrations, audio narration, and book
// manifests from the warehouse to every child's device, fast.
//
// Three components:
//   1. S3 Bucket Configuration: where content is stored, with lifecycle
//      policies, versioning, and CORS for direct browser uploads
//   2. CloudFront Distribution: the CDN edge network that caches content
//      at 400+ locations worldwide, ensuring a child in rural Australia
//      gets their storybook illustrations as fast as one in Sydney
//   3. Secrets Management: centralised, encrypted storage for all API keys
//      (Claude, ElevenLabs, GPT Image) with automatic rotation
//
// The critical integration: Sprint 18's FileStorageService (S18-003)
// defined the interface — StorageConfig, upload(), getPresignedUploadUrl().
// This sprint provisions the actual S3 bucket and CloudFront distribution
// that make those methods work against real AWS infrastructure.
// ============================================================================

import { ScholarlyBaseService, Result, ok, fail } from '../shared/base';

// ==========================================================================
// Section 1: S3 Bucket Configuration
// ==========================================================================
// The S3 bucket is the canonical store for all storybook content. Every
// illustration generated by the illustration pipeline (S20-002), every
// audio narration from ElevenLabs (Sprint 21), and every book manifest
// passes through this bucket. The path hierarchy mirrors Sprint 18's
// StoragePath type:
//   tenants/{tenantId}/storybooks/{bookId}/illustrations/page-{n}.webp
//   tenants/{tenantId}/storybooks/{bookId}/audio/page-{n}.mp3
//   tenants/{tenantId}/storybooks/{bookId}/manifest.json
//   tenants/{tenantId}/characters/{charId}/style-sheet.png
//   seed-library/{bookId}/{asset}

export interface S3BucketConfig {
  readonly bucketName: string;
  readonly region: string;
  readonly environment: string;
  readonly enableVersioning: boolean;
  readonly enableEncryption: boolean;
  readonly encryptionKeyArn?: string;        // KMS key for SSE-KMS
  readonly corsAllowedOrigins: string[];     // For direct browser uploads
  readonly lifecyclePolicies: LifecyclePolicy[];
  readonly replicationConfig?: ReplicationConfig;
  readonly accessLogging: AccessLoggingConfig;
  readonly intelligentTiering: boolean;      // Auto-move to cheaper storage
  readonly blockPublicAccess: boolean;       // Always true — CloudFront serves
}

export interface LifecyclePolicy {
  readonly id: string;
  readonly prefix: string;                   // e.g. 'tenants/' or 'seed-library/'
  readonly transitions: StorageTransition[];
  readonly expirationDays?: number;          // Delete after N days (for temp uploads)
  readonly abortIncompleteMultipartDays: number;
}

export interface StorageTransition {
  readonly days: number;
  readonly storageClass: 'STANDARD_IA' | 'GLACIER_IR' | 'GLACIER' | 'DEEP_ARCHIVE';
}

export interface ReplicationConfig {
  readonly destinationBucket: string;
  readonly destinationRegion: string;        // Cross-region for disaster recovery
  readonly replicateEncryption: boolean;
}

export interface AccessLoggingConfig {
  readonly enabled: boolean;
  readonly targetBucket: string;
  readonly targetPrefix: string;
}

