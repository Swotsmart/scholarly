// ==========================================================================
// Section 2: CloudFront Distribution Configuration
// ==========================================================================
// CloudFront is the CDN that sits between S3 and the user's device. When
// a child opens a storybook, their browser doesn't fetch illustrations
// from the S3 bucket in Sydney — it fetches from the nearest CloudFront
// edge location (which might be Perth, Melbourne, or Singapore). After
// the first request, CloudFront caches the content at the edge for
// subsequent readers.
//
// The Origin Access Identity (OAI) is a security mechanism: only
// CloudFront can read from the S3 bucket. Direct S3 URLs don't work.
// This prevents content leakage and ensures all access is logged,
// metered, and cacheable.

export interface CloudFrontConfig {
  readonly distributionName: string;
  readonly environment: string;
  readonly originBucket: string;
  readonly originRegion: string;
  readonly aliases: string[];               // Custom domain names
  readonly certificateArn?: string;          // ACM cert for HTTPS on custom domain
  readonly priceClass: 'PriceClass_100' | 'PriceClass_200' | 'PriceClass_All';
  readonly defaultTtlSeconds: number;
  readonly maxTtlSeconds: number;
  readonly minTtlSeconds: number;
  readonly cachePolicies: CachePolicyConfig[];
  readonly geoRestrictions: GeoRestrictionConfig;
  readonly wafWebAclArn?: string;            // Web Application Firewall
  readonly enableAccessLogs: boolean;
  readonly logBucket: string;
  readonly responseHeaders: ResponseHeadersConfig;
  readonly errorPages: ErrorPageConfig[];
  readonly enableCompression: boolean;
  readonly enableHttp2: boolean;
  readonly enableHttp3: boolean;
}

export interface CachePolicyConfig {
  readonly pathPattern: string;             // e.g. '/tenants/*/illustrations/*'
  readonly ttlSeconds: number;
  readonly compress: boolean;
  readonly allowedMethods: ('GET' | 'HEAD' | 'OPTIONS' | 'PUT' | 'POST')[];
  readonly cachedMethods: ('GET' | 'HEAD')[];
  readonly forwardHeaders: string[];        // Headers to include in cache key
  readonly forwardQueryStrings: boolean;
}

export interface GeoRestrictionConfig {
  readonly type: 'none' | 'whitelist' | 'blacklist';
  readonly countries: string[];             // ISO 3166-1 alpha-2 codes
}

export interface ResponseHeadersConfig {
  readonly securityHeaders: boolean;        // Strict-Transport-Security, X-Content-Type-Options
  readonly corsHeaders: boolean;
  readonly customHeaders: Record<string, string>;
}

export interface ErrorPageConfig {
  readonly errorCode: number;
  readonly responseCode: number;
  readonly responsePagePath: string;
  readonly cacheTtlSeconds: number;
}

