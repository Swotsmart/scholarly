// =============================================================================
// ONBOARDING INTEGRATION
// =============================================================================
// Connects the onboarding flow to the self-composing interface.
//
// New users arrive with an empty menu (except anchors). The onboarding
// flow is their guided tour — and each step they complete gives the
// seed engine data to work with. When onboarding finishes, the seed
// engine runs for the first time, using the preferences and interests
// collected during onboarding to populate "Suggested for you."
//
// Think of onboarding as the first day at a new job. The office tour
// (onboarding steps) shows you where things are. As you visit each
// department, your mental map fills in. At the end of the day, your
// desk is arranged with the tools you'll need tomorrow — that's the
// initial seed run.
//
// Specification references:
//   Phase 6 plan — "Onboarding integration: 1–2 days —
//                    Onboarding completion triggers initial seed engine run.
//                    Onboarding steps map to first-use of tasks."
//   Phase 3 — Seed engine scoring algorithm (5 signals)
//   Phase 1 — composing-menu-store.ts (recordUse, addSeeds)
//
// Integration points:
//   - Onboarding store (existing): completion status, selected preferences
//   - seed-engine.service.ts (Phase 3): runSeedEngine
//   - composing-menu-store.ts (Phase 1): recordUse, addSeeds
//   - menu-sync.service.ts (Phase 6): saveToServer after initial population
// =============================================================================

// =============================================================================
// TYPES
// =============================================================================

/**
 * Data collected during onboarding that informs the seed engine.
 * This maps to the onboarding store's state.
 */
export interface OnboardingProfile {
  /** The user's selected role during onboarding. */
  role: string;

  /** Subject areas of interest (e.g., "Mathematics", "English", "Science"). */
  interests: string[];

  /** Year levels they work with (e.g., ["Year 1", "Year 3"]). */
  yearLevels: string[];

  /** Features they expressed interest in during onboarding. */
  selectedFeatures: string[];

  /** How they described their tech comfort level. */
  techComfort: 'beginner' | 'intermediate' | 'advanced';

  /** Whether they manage other users (teachers, students). */
  hasTeam: boolean;

  /** Whether they use the platform at an institution or independently. */
  isInstitutional: boolean;

  /** Timestamps of each onboarding step completion. */
  stepCompletions: Record<string, string>;
}

/**
 * Mapping from onboarding steps to task references.
 * When a user completes an onboarding step that involves a task,
 * we fire recordUse() to give the composing store a head start.
 */
export interface OnboardingStepMapping {
  /** The onboarding step identifier. */
  stepId: string;

  /** The task reference this step corresponds to. */
  taskRef: string;

  /** Whether completing this step should count as a "use" of the task. */
  countsAsUse: boolean;

  /**
   * Weight multiplier for seed scoring. Steps the user showed
   * particular interest in get a higher weight.
   */
  seedWeight: number;
}

/**
 * The result of processing onboarding completion.
 */
export interface OnboardingProcessResult {
  /** Whether processing completed successfully. */
  success: boolean;

  /** Number of tasks that received recordUse() from onboarding steps. */
  tasksUsed: number;

  /** Number of seeds generated by the initial seed engine run. */
  seedsGenerated: number;

  /** The task refs that were seeded. */
  seededRefs: string[];

  /** Any errors encountered during processing. */
  errors: string[];
}

