// ONBOARDING PROCESSOR
// =============================================================================

/**
 * Processes onboarding completion and bridges it to the composing interface.
 *
 * This is a stateless processor — it takes the onboarding profile and
 * the store/engine interfaces, and performs the one-time setup.
 */
export class OnboardingProcessor {
  private readonly stepMappings: OnboardingStepMapping[];

  constructor(stepMappings: OnboardingStepMapping[] = DEFAULT_STEP_MAPPINGS) {
    this.stepMappings = stepMappings;
  }

  /**
   * Process onboarding completion. Called once when the user finishes
   * the onboarding flow.
   *
   * Steps:
   * 1. Map completed onboarding steps to task uses (recordUse)
   * 2. Compute interest-based seed boosts
   * 3. Trigger the initial seed engine run
   * 4. Apply seeds to the composing store
   */
  async processOnboardingComplete(
    profile: OnboardingProfile,
    actions: {
      recordUse: (ref: string) => void;
      addSeeds: (seeds: Array<{ ref: string; score: number }>) => void;
      runSeedEngine: (
        role: string,
        context: {
          interests: string[];
          yearLevels: string[];
          techComfort: string;
          isInstitutional: boolean;
          interestBoosts: Record<string, number>;
        },
      ) => Promise<Array<{ ref: string; score: number }>>;
      saveToServer: () => Promise<void>;
    },
  ): Promise<OnboardingProcessResult> {
    const errors: string[] = [];
    let tasksUsed = 0;
    let seedsGenerated = 0;
    const seededRefs: string[] = [];

    try {
      // ── Step 1: Map onboarding steps to task uses ──

      for (const mapping of this.stepMappings) {
        if (!mapping.countsAsUse) continue;

        // Check if this step was completed during onboarding
        if (profile.stepCompletions[mapping.stepId]) {
          try {
            actions.recordUse(mapping.taskRef);
            tasksUsed++;
          } catch (err) {
            errors.push(
              `Failed to record use for ${mapping.taskRef}: ${
                err instanceof Error ? err.message : 'Unknown error'
              }`,
            );
          }
        }
      }

      // ── Step 2: Compute interest-based boosts ──

      const interestBoosts: Record<string, number> = {};

      for (const interest of profile.interests) {
        const boostedTasks = INTEREST_TASK_BOOSTS[interest];
        if (!boostedTasks) continue;

        for (const taskRef of boostedTasks) {
          interestBoosts[taskRef] = (interestBoosts[taskRef] ?? 0) + 0.15;
        }
      }

      // Also boost tasks from completed onboarding steps
      for (const mapping of this.stepMappings) {
        if (profile.stepCompletions[mapping.stepId]) {
          interestBoosts[mapping.taskRef] =
            (interestBoosts[mapping.taskRef] ?? 0) + (mapping.seedWeight * 0.1);
        }
      }

      // ── Step 3: Trigger initial seed engine run ──

      try {
        const seeds = await actions.runSeedEngine(profile.role, {
          interests: profile.interests,
          yearLevels: profile.yearLevels,
          techComfort: profile.techComfort,
          isInstitutional: profile.isInstitutional,
          interestBoosts,
        });

        seedsGenerated = seeds.length;
        seededRefs.push(...seeds.map(s => s.ref));

        // ── Step 4: Apply seeds to the composing store ──

        if (seeds.length > 0) {
          actions.addSeeds(seeds);
        }
      } catch (err) {
        errors.push(
          `Seed engine failed: ${err instanceof Error ? err.message : 'Unknown error'}`,
        );
      }

      // ── Step 5: Save to server ──

      try {
        await actions.saveToServer();
      } catch (err) {
        // Non-fatal — local state is correct, will sync later
        errors.push(
          `Server save failed (will retry): ${
            err instanceof Error ? err.message : 'Unknown error'
          }`,
        );
      }

      return {
        success: errors.length === 0,
        tasksUsed,
        seedsGenerated,
        seededRefs,
        errors,
      };
    } catch (err) {
      return {
        success: false,
        tasksUsed,
        seedsGenerated,
        seededRefs,
        errors: [
          ...errors,
          `Onboarding processing failed: ${
            err instanceof Error ? err.message : 'Unknown error'
          }`,
        ],
      };
    }
  }

  /**
   * Get the tasks that should be pre-activated based on the user's role
   * and tech comfort level. These aren't seeds — they're items that
   * start in the ACTIVE state immediately, giving the user a working
   * menu from day one.
   *
   * A beginner teacher gets fewer items (less overwhelming).
   * An advanced teacher gets more items (they can handle the density).
   */
  getInitialActiveItems(
    role: string,
    techComfort: 'beginner' | 'intermediate' | 'advanced',
  ): string[] {
    const roleDefaults: Record<string, Record<string, string[]>> = {
      teacher: {
        beginner: ['T0', 'T2'],               // Dashboard, Classes
        intermediate: ['T0', 'T2', 'T3'],     // + Gradebook
        advanced: ['T0', 'T2', 'T3', 'T5'],   // + Attendance
      },
      parent: {
        beginner: ['P0', 'P1'],                // Dashboard, Children
        intermediate: ['P0', 'P1', 'P2'],     // + Progress
        advanced: ['P0', 'P1', 'P2', 'P3'],   // + Messages
      },
      learner: {
        beginner: ['L0'],                      // Home only
        intermediate: ['L0', 'L1'],            // + Learn
        advanced: ['L0', 'L1', 'L3'],          // + Arena
      },
      tutor: {
        beginner: ['TU0', 'TU1'],              // Dashboard, Sessions
        intermediate: ['TU0', 'TU1', 'TU2'],  // + Students
        advanced: ['TU0', 'TU1', 'TU2', 'TU3'], // + Earnings
      },
      admin: {
        beginner: ['A0', 'A1'],                // Dashboard, Users
        intermediate: ['A0', 'A1', 'A2'],     // + Reports
        advanced: ['A0', 'A1', 'A2', 'A3'],   // + Settings
      },
    };

    return roleDefaults[role]?.[techComfort] ?? roleDefaults[role]?.['beginner'] ?? [];
  }

  /**
   * Determine which step mappings apply to a given role.
   */
  getMappingsForRole(role: string): OnboardingStepMapping[] {
    const rolePrefixes: Record<string, string[]> = {
      teacher: ['tour_', 'create_', 'invite_'],
      parent: ['link_', 'tour_progress', 'tour_messages', 'set_'],
      learner: ['choose_', 'first_', 'tour_arena', 'tour_storybooks', 'earn_'],
      tutor: ['tour_'],
      admin: ['tour_'],
    };

    const prefixes = rolePrefixes[role] ?? [];
    return this.stepMappings.filter(
      m => prefixes.some(p => m.stepId.startsWith(p)),
    );
  }
}
